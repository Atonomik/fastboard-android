package io.agora.board.fast.model;

/**
 * @author fenglibin
 */
public enum FastRegion {
    /**
     * `CN_HZ`：中国杭州。
     * <p>
     * 该数据中心为其他数据中心服务区未覆盖的地区提供服务。
     */
    CN_HZ,

    /**
     * `US_SV`：美国硅谷。
     * <p>
     * 该数据中心为北美洲、南美洲地区提供服务。
     */
    US_SV,

    /**
     * `SG`：新加坡。
     * <p>
     * 该数据中心为新加坡、东亚、东南亚地区提供服务。
     */
    SG,

    /**
     * `IN_MUM`：印度孟买。
     * <p>
     * 该数据中心为印度地区提供服务。
     */
    IN_MUM,

    /**
     * `GB_LON`：英国伦敦。
     * <p>
     * 该数据中心为欧洲地区提供服务。
     */
    GB_LON;
}
